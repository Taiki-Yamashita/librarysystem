package beans;

public class Book {

	private int id;
	private String name;
	private String author;
	private String publisher;
	private String category;
	private String type;
	private String publishedDate;
	private String libraryId;
	private String shelfId;
	private String isbnId;
	private String keeping;
	private String lending;
	private String reserving;
	private String disposing;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPublishedDate() {
		return publishedDate;
	}
	public void setPublishedDate(String publishedDate) {
		this.publishedDate = publishedDate;
	}
	public String getLibraryId() {
		return libraryId;
	}
	public void setLibraryId(String libraryId) {
		this.libraryId = libraryId;
	}
	public String getShelfId() {
		return shelfId;
	}
	public void setShelfId(String shelfId) {
		this.shelfId = shelfId;
	}
	public String getIsbnId() {
		return isbnId;
	}
	public void setIsbnId(String isbnId) {
		this.isbnId = isbnId;
	}
	public String getKeeping() {
		return keeping;
	}
	public void setKeeping(String keeping) {
		this.keeping = keeping;
	}
	public String getLending() {
		return lending;
	}
	public void setLending(String lending) {
		this.lending = lending;
	}
	public String getReserving() {
		return reserving;
	}
	public void setReserving(String reserving) {
		this.reserving = reserving;
	}
	public String getDisposing() {
		return disposing;
	}
	public void setDisposing(String disposing) {
		this.disposing = disposing;
	}

}